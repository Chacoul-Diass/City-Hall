    public int nombreAleatoire(){
        
        boolean match ;

        int min = 1000 ;
        int max = 150000 ;
        int random_number = 0 ;

        Random rand = new Random() ;

        try{

            Connection cnect ;
            ResultSet result_random ;
            PreparedStatement pst_random ;

            do{

                random_number = rand.nextInt(max - min + 1) + min ;

                Class.forName("com.mysql.jdbc.Driver") ;

                String url = "jdbc:mysql://localhost:3306/mairie?user=root&password=" ;
                String user = "root" ;
                String pwd = "" ;

                cnect = DriverManager.getConnection(url, user, pwd) ;

                pst_random = cnect.prepareStatement("SELECT * FROM extrait WHERE num_registre = ?") ;
                pst_random.setInt(1, random_number) ;

                result_random = pst_random.executeQuery() ;

                match = result_random.next() ;

                pst_random.close();
                result_random.close();
                cnect.close();

            }while(match) ;

        }catch(Exception ex){
            ex.printStackTrace();
        }
        return random_number ;
    }

    
    public boolean champRempli(){
        
        boolean rempli = true ;
        
        if(nom.getText().equals("") || prenom.getText().equals("") || heureNaissance.getText().equals("")){
            
            rempli = false ;
        }
        
        if(maternite.getText().equals("") || libelle.getText().equals("") || noRegistre.getText().equals("")){
            
            rempli = false;
        }
        if(lieuNaissance.equals("") || nomMere.equals("") || prenomMere.equals("") || professionMere.getText().equals("")){
            
            rempli = false ;
        }
        if(professionPere.getText().equals("") || nomPere.getText().equals("") || prenomPere.getText().equals("")){
            
            rempli = false ;
        }
        
        if(professionPere.getText().equals("") || nomPere.getText().equals("") || prenomPere.getText().equals("")){
            
            rempli = false ;
        }
        if(((JTextField)dateNaiss.getDateEditor().getUiComponent()).getText().equals("0001-01-01") || ((JTextField)dateDeclaration.getDateEditor().getUiComponent()).getText().equals("0001-01-01")){
            JOptionPane.showMessageDialog(null, "Veuillez modifier les dates!!!");
            
        }
        if(annee.getYear() == Calendar.YEAR){
            JOptionPane.showMessageDialog(null, "Veuillez modifier les dates!!!");
            
        }
        if(sexe.getSelectedItem().toString().equals("selectionnez") || nationalitePere.getSelectedItem().toString().equals("Selectionnez") || nationalitePere.getSelectedItem().toString().equals("Selectionnez")){
            rempli = false;
        }
              
        if(imagePathSignature == null || imagePathTimbre == null || imagePathDeclarat == null || imagePathPere == null || imagePathPere == null){
            
            rempli = false ;
        }
        
        return rempli ;
    }
    
