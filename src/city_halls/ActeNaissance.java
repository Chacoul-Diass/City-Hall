            do{

                random_number = rand.nextInt(max - min + 1) + min ;

                Class.forName("com.mysql.jdbc.Driver") ;

                String url = "jdbc:mysql://localhost:3306/mairie?user=root&password=" ;
                String user = "root" ;
                String pwd = "" ;

                cnect = DriverManager.getConnection(url, user, pwd) ;

                pst_random = cnect.prepareStatement("SELECT * FROM extrait WHERE num_registre = ?") ;
                pst_random.setInt(1, random_number) ;

                result_random = pst_random.executeQuery() ;

                match = result_random.next() ;

                pst_random.close();
                result_random.close();
                cnect.close();

            }while(match) ;

